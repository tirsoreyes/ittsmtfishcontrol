package edu.itssmt.model;

import java.util.List;

public class MisHorarios {
	
	private List<Horario> lunes;
	private List<Horario> martes;
	private List<Horario> miercoles;
	private List<Horario> jueves;
	private List<Horario> viernes;
	private List<Horario> sabados;
	private List<Horario> domingos;
	
	
	public List<Horario> getLunes() {
		return lunes;
	}
	public void setLunes(List<Horario> lunes) {
		this.lunes = lunes;
	}
	public List<Horario> getMartes() {
		return martes;
	}
	public void setMartes(List<Horario> martes) {
		this.martes = martes;
	}
	public List<Horario> getMiercoles() {
		return miercoles;
	}
	public void setMiercoles(List<Horario> miercoles) {
		this.miercoles = miercoles;
	}
	public List<Horario> getJueves() {
		return jueves;
	}
	public void setJueves(List<Horario> jueves) {
		this.jueves = jueves;
	}
	public List<Horario> getViernes() {
		return viernes;
	}
	public void setViernes(List<Horario> viernes) {
		this.viernes = viernes;
	}
	public List<Horario> getSabados() {
		return sabados;
	}
	public void setSabados(List<Horario> sabados) {
		this.sabados = sabados;
	}
	public List<Horario> getDomingos() {
		return domingos;
	}
	public void setDomingos(List<Horario> domingos) {
		this.domingos = domingos;
	}
	
	
	

}
